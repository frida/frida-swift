def generate_signal_handler(otype: ObjectType, signal, model: Model) -> str:
    name = otype.swift_name
    handler = "on" + to_pascal(signal.event_name)

    args = [(param, f"arg{i}") for i, param in enumerate(signal.parameters)]
    closure_params = ["_"] + [var for _, var in args] + ["userData"]

    sig_types = (["OpaquePointer"]
                 + [signal_param_kind(param, model)[0] for param in signal.parameters]
                 + ["gpointer"])
    sig = f"@convention(c) ({', '.join(sig_types)}) -> Void"

    lines = [
        f"        let connection = Unmanaged<SignalConnection<{name}>>"
        f".fromOpaque(userData).takeUnretainedValue()",
        "        guard let instance = connection.instance else { return }",
    ]

    var_by_param = {}
    for i, (param, var) in enumerate(args):
        expr_lines, local = marshal_signal_param(param, var, model)
        lines.extend("        " + l for l in expr_lines)
        transform = signal.transform.get(i)
        if transform is not None:
            _, converter = transform
            parsed = f"{param.swift_name}Parsed"
            lines.append(f"        guard let {parsed} = try? {converter}({local}) else {{ return }}")
            local = parsed
        var_by_param[param.name] = local

    payload = signal.event_payload
    if not payload:
        arg_str = ""
    else:
        parts = []
        for label, param in payload:
            v = var_by_param[param.name]
            parts.append(f"{label}: {v}" if label else v)
        arg_str = "(" + ", ".join(parts) + ")"

    if signal.terminal:
        lines.append(f"        instance.eventSource.finish(replayLast: .{signal.event_name}{arg_str})")
    else:
        lines.append(f"        instance.publish(.{signal.event_name}{arg_str})")

    body = "\n".join(lines)
    return (f"    private let {handler}: {sig} = "
            f"{{ {', '.join(closure_params)} in\n{body}\n    }}")


def marshal_signal_param(param: Parameter, var: str, model: Model):
    n = param.type.name
    nn = param.nullable
    local = f"{param.swift_name}Value"
    if n == "utf8":
        if nn:
            return ([f"let {local} = {var} != nil ? String(cString: {var}!) : nil"], local)
        return ([f"let {local} = String(cString: {var})"], local)
    if n == "GLib.Bytes":
        suffix = "" if nn else "!"
        return ([f"let {local} = Marshal.arrayFromBytes({var}){suffix}"], local)
    if n == "guint":
        return ([], f"UInt({var})")
    if n in ("gint", "gint32"):
        return ([], f"Int({var})")
    if n == "gdouble":
        return ([], f"Double({var})")
    enum = model.enumerations.get(n.split(".")[-1])
    if enum is not None and not enum.drop:
        return ([], f"{enum.swift_name}(rawValue: numericCast({var}))!")
    obj = model.swift_class_for(n)
    if nn:
        lines = [
            f"var {local}: {obj.swift_name}? = nil",
            f"if let {var} = {var} {{",
            f"    g_object_ref(gpointer({var}))",
            f"    {local} = {obj.swift_name}(handle: {var})",
            f"}}",
        ]
        return (lines, local)
    lines = [
        f"g_object_ref(gpointer({var}))",
        f"let {local} = {obj.swift_name}(handle: {var})",
    ]
    return (lines, local)
